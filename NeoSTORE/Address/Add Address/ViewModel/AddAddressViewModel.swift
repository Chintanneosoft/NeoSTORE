import UIKit


//MARK: - CartViewModelDelegate Protocol
protocol AddAddressViewModelDelegate:NSObject{
    func successAddress(msg: String)
    func failureAddress(msg: String)
}

//MARK: - CartViewModel
class AddAddressViewModel: NSObject {
    
    //MARK: - CartViewModelDelegate Object Declare
    weak var addAddressViewModelDelegate: AddAddressViewModelDelegate?
    
    //APIService Object
    private let addAddressAPIService = AddAddressAPIService()
    
    func addAddress(address: String){
        addAddressAPIService.addAddress(addess: address){
            response in
            switch response{
            case .success(let value):
                print(value)
                
                self.addAddressViewModelDelegate?.successAddress(msg: value.user_msg)
                
            case .failure(let error):
                print(error)
                self.addAddressViewModelDelegate?.failureAddress(msg: error.localizedDescription)
            }
        }
    }
    
}
