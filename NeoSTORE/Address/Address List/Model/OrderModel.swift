import UIKit

//MARK: - Order
struct Order: Codable {
    let status: Int
    let message,userMsg : String
    
    enum CodingKeys: String, CodingKey {
        case status, message
        case userMsg = "user_msg"
    }
}

//MARK: - Order
struct OrderList: Codable {
    let status: Int
    let data: [OrderData]?
    let message,userMsg : String
    
    enum CodingKeys: String, CodingKey {
        case status, message, data
        case userMsg = "user_msg"
    }
}

// MARK: - OrderData
struct OrderData: Codable {
    let id: Int
    let cost: Double
    let created: String
}
